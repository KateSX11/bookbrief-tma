def main():
    engine = create_engine(DB_URL, connect_args={"check_same_thread": False})
    Base.metadata.create_all(bind=engine)
    Session = sessionmaker(bind=engine)
    session = Session()

    session.query(ReadingHistory).delete()
    session.query(Chapter).delete()
    session.query(Book).delete()

    for book_data in BOOKS:
        print(f"  Seeding: {book_data['title']}")
        book = Book(
            id=book_data["id"],
            title=book_data["title"],
            author=book_data["author"],
            original_title=book_data.get("original_title"),
            category=book_data["category"],
            tagline=book_data["tagline"],
            quotes=json.dumps(book_data["quotes"], ensure_ascii=False),
            time=book_data["time"],
            cover_url=None,
            is_featured=book_data["is_featured"],
            is_hot=book_data["is_hot"],
            is_free=book_data["is_free"],
            sort_order=book_data["sort_order"],
            status="published",
        )
        session.add(book)
        session.flush()

        for ch_data in book_data["chapters"]:
            chapter = Chapter(
                book_id=book_data["id"],
                index=ch_data["index"],
                title=ch_data["title"],
                content=ch_data["content"],
                audio_url=None,
            )
            session.add(chapter)

    session.commit()
    count = session.query(Book).count()
    ch_count = session.query(Chapter).count()
    print(f"\nDone! {count} books, {ch_count} chapters seeded into {DB_PATH}")
    session.close()


